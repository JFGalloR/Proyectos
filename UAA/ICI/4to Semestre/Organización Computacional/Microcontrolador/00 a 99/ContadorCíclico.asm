CUENTA_DECENAS:
	MOV P0, #11000000B
	CALL CUENTA_UNIDADES
	MOV P0, #11111001B
	CALL CUENTA_UNIDADES
	MOV P0, #10100100B
	CALL CUENTA_UNIDADES
	MOV P0, #10110000B
	CALL CUENTA_UNIDADES
	MOV P0, #10011001B
	CALL CUENTA_UNIDADES
	MOV P0, #10010010B
	CALL CUENTA_UNIDADES
	MOV P0, #10000010B
	CALL CUENTA_UNIDADES
	MOV P0, #11111000B
	CALL CUENTA_UNIDADES
	MOV P0, #10000000B
	CALL CUENTA_UNIDADES
	MOV P0, #10010000B
	CALL CUENTA_UNIDADES
	JMP CUENTA_DECENAS

CUENTA_UNIDADES:
	MOV P2, #11000000B
	CALL DELAY
	MOV P2, #11111001B
	CALL DELAY
	MOV P2, #10100100B
	CALL DELAY
	MOV P2, #10110000B
	CALL DELAY
	MOV P2, #10011001B
	CALL DELAY
	MOV P2, #10010010B
	CALL DELAY
	MOV P2, #10000010B
	CALL DELAY
	MOV P2, #11111000B
	CALL DELAY
	MOV P2, #10000000B
	CALL DELAY
	MOV P2, #10010000B
	CALL DELAY
	RET

DELAY:
	MOV R2, #8
CICLO3:	MOV R1, #0FFH
CICLO2: MOV R0, #0FFH
CICLO1: DJNZ R0, CICLO1
	DJNZ R1, CICLO2
	DJNZ R2, CICLO3
	RET
END
